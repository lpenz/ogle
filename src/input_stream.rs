use color_eyre::Result;
use pin_project_lite::pin_project;
use std::collections::VecDeque;
use std::pin::Pin;
use std::process::ExitStatus;
use std::task::{Context, Poll};
use tokio_stream::Stream;
use tokio_stream::wrappers::IntervalStream;

use crate::sys_input;
use crate::sys_input::Cmd;
use crate::sys_input::ProcessStream;
use crate::sys_input::SysInputApi;
use crate::time_wrapper::Duration;

#[derive(Debug, Clone, PartialEq, Eq)]
pub enum InputItem {
    LineOut(String),
    LineErr(String),
    Done(ExitStatus),
    Err(std::io::ErrorKind),
    Tick,
}

impl From<sys_input::Item> for InputItem {
    fn from(item: sys_input::Item) -> Self {
        match item {
            sys_input::Item::Stdout(l) => InputItem::LineOut(l),
            sys_input::Item::Stderr(l) => InputItem::LineErr(l),
            sys_input::Item::Done(Ok(sts)) => InputItem::Done(sts),
            sys_input::Item::Done(Err(e)) => InputItem::Err(e),
        }
    }
}

impl From<tokio::time::Instant> for InputItem {
    fn from(_: tokio::time::Instant) -> Self {
        InputItem::Tick
    }
}

impl From<std::io::Error> for InputItem {
    fn from(e: std::io::Error) -> Self {
        InputItem::Err(e.kind())
    }
}

pin_project! {
#[derive(Default)]
pub struct InputStream<SI: SysInputApi> {
    sys_input: SI,
    cmd: Cmd,
    // Events coming from the running process
    process: Option<ProcessStream>,
    // Tick events generated by the [`IntervalStream`] timer
    ticker: Option<IntervalStream>,
    // Used for tests
    testlist: VecDeque<InputItem>,
}
}

impl<SI: SysInputApi> InputStream<SI> {
    pub fn new(sys_input: SI, cmd: Cmd, refresh_delay: Duration) -> Result<Self> {
        Ok(Self {
            sys_input,
            cmd,
            process: None,
            ticker: Some(IntervalStream::new(tokio::time::interval(
                refresh_delay.into(),
            ))),
            testlist: Default::default(),
        })
    }
}

impl<SI: SysInputApi> Stream for InputStream<SI> {
    type Item = InputItem;

    fn poll_next(self: Pin<&mut Self>, cx: &mut Context<'_>) -> Poll<Option<Self::Item>> {
        let this = self.project();
        if let Some(item) = this.testlist.pop_front() {
            return Poll::Ready(Some(item));
        }
        if this.process.is_none() {
            // Run the process if it's not running
            match this.sys_input.run_command(this.cmd.clone()) {
                Ok(process) => *this.process = Some(process),
                Err(e) => return Poll::Ready(Some(InputItem::Err(e.kind()))),
            }
        }
        if let Some(process) = this.process {
            match Pin::new(process).poll_next(cx) {
                Poll::Ready(Some(item)) => {
                    return Poll::Ready(Some(item.into()));
                }
                Poll::Ready(None) => {
                    *this.process = None;
                    *this.ticker = None;
                }
                Poll::Pending => {}
            }
        }
        if let Some(ticker) = this.ticker {
            match Pin::new(ticker).poll_next(cx) {
                Poll::Ready(Some(item)) => Poll::Ready(Some(item.into())),
                _ => Poll::Pending,
            }
        } else {
            Poll::Ready(None)
        }
    }
}

#[cfg(test)]
mod tests {
    use clap::Parser;
    use color_eyre::Result;
    use color_eyre::eyre::eyre;
    use tokio_stream::StreamExt;

    use crate::cli::Cli;
    use crate::sys_input;
    use crate::sys_input::SysInputReal;
    use crate::sys_input::SysInputVirtual;

    use super::*;

    async fn stream_cmd(
        cmd: &[&str],
    ) -> Result<impl StreamExt<Item = InputItem> + std::marker::Unpin + Send + 'static> {
        let duration = Duration::milliseconds(5000);
        let cli = Cli::try_parse_from(cmd)?;
        let sys_input = SysInputReal::default();
        InputStream::new(sys_input, cli.get_cmd(), duration)
    }

    async fn stream_next<T>(stream: &mut T) -> Result<InputItem>
    where
        T: StreamExt<Item = InputItem> + std::marker::Unpin + Send + 'static,
    {
        while let Some(item) = stream.next().await {
            match item {
                InputItem::Tick => {
                    continue;
                }
                _ => {
                    return Ok(item);
                }
            }
        }
        Err(eyre!("no item received"))
    }

    #[tokio::test]
    async fn test_true() -> Result<()> {
        let mut stream = stream_cmd(&["ogle", "true"]).await?;
        let item = stream_next(&mut stream).await?;
        let InputItem::Done(sts) = item else {
            return Err(eyre!("unexpected stream item {:?}", item));
        };
        assert!(sts.success());
        Ok(())
    }

    #[tokio::test]
    async fn test_false() -> Result<()> {
        let mut stream = stream_cmd(&["ogle", "false"]).await?;
        let item = stream_next(&mut stream).await?;
        let InputItem::Done(sts) = item else {
            return Err(eyre!("unexpected stream item {:?}", item));
        };
        assert!(!sts.success());
        Ok(())
    }

    #[tokio::test]
    async fn test_echo() -> Result<()> {
        let mut stream = stream_cmd(&["ogle", "echo", "test"]).await?;
        let item = stream_next(&mut stream).await?;
        let InputItem::LineOut(s) = item else {
            return Err(eyre!("unexpected stream item {:?}", item));
        };
        assert_eq!(s, "test");
        let item = stream_next(&mut stream).await?;
        let InputItem::Done(sts) = item else {
            return Err(eyre!("unexpected stream item {:?}", item));
        };
        assert!(sts.success());
        Ok(())
    }

    #[tokio::test]
    async fn test_stderr() -> Result<()> {
        let mut stream = stream_cmd(&["ogle", "--", "/bin/sh", "-c", "echo test >&2"]).await?;
        let item = stream_next(&mut stream).await?;
        let InputItem::LineErr(s) = item else {
            return Err(eyre!("unexpected stream item {:?}", item));
        };
        assert_eq!(s, "test");
        let item = stream_next(&mut stream).await?;
        let InputItem::Done(sts) = item else {
            return Err(eyre!("unexpected stream item {:?}", item));
        };
        assert!(sts.success());
        Ok(())
    }

    #[tokio::test]
    async fn test_testlist() -> Result<()> {
        let list = vec![
            sys_input::Item::Stdout("stdout".into()),
            sys_input::Item::Stderr("stderr".into()),
            sys_input::Item::Done(Ok(ExitStatus::default())),
        ];
        let mut sys_input = SysInputVirtual::default();
        sys_input.set_items(list);
        let streamer = InputStream::new(sys_input, Cmd::default(), Duration::MAX)?;
        let streamed = streamer.collect::<Vec<_>>().await;
        assert_eq!(
            streamed,
            vec![
                InputItem::LineOut("stdout".into()),
                InputItem::LineErr("stderr".into()),
                InputItem::Done(ExitStatus::default()),
            ]
        );
        Ok(())
    }
}
